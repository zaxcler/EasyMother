package com.easymother.bean;

public class Blocks {
	private int id;

	private String name;

	private String image;

	private int scannerAmount;

	private int allAmount;

	private int dayAmount;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public int getScannerAmount() {
		return scannerAmount;
	}

	public void setScannerAmount(int scannerAmount) {
		this.scannerAmount = scannerAmount;
	}

	public int getAllAmount() {
		return allAmount;
	}

	public void setAllAmount(int allAmount) {
		this.allAmount = allAmount;
	}

	public int getDayAmount() {
		return dayAmount;
	}

	public void setDayAmount(int dayAmount) {
		this.dayAmount = dayAmount;
	}
	

}
